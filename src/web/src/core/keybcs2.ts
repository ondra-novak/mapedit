const keybcs2_to_windows1250=(()=>{
    const r = [];
    for (let i = 0; i < 256; ++i) r.push(i);
    for (const [k,w] of czxlattab) {
        r[k] = w;
    }
    return r;
})();

const windows1250_to_keybcs2=(()=>{
    const r = [];
    for (let i = 0; i < 256; ++i) r.push(i);
    for (const [k,w] of czxlattab) {
        r[w] = k;
    }
    return r;
})();

const charcode_to_keybcs2=(()=>{
    const r = new Map();
    for (let i = 0; i < windows1250_chars.length; ++i) {
        let cd = windows1250_chars.charCodeAt(i);
        if (cd == 32) cd = i+128;
        const kb = windows1250_to_keybcs2[i+128];
        r.set(cd, kb);
    }
    return r;
})();

export function string_from_keybcs2(source:number[]) {
    const str = [];
    for (const v of source) {
        str.push(keybcs2_to_windows1250[v]);
    }
    return (new TextDecoder("windows-1250")).decode(new Uint8Array(str));
}

export function keybcs2_from_string(source:string) {

    const res = [];
    for (const v of source) {
        const k = v.charCodeAt(0);
        let c = k<128?k:charcode_to_keybcs2.get(k);
        if (!c) c = '?';
        res.push(c);
    }
    return res;
}